		  unlock_flash();

		  // I reserved 992 KB for applications. That means page 16-255 and bank 2
		  // Firstly erase old application from flash.
		  for(int i=16 ; i<256 ; i++) {
		      erase_page(i);
		 	}
		  erase_bank(2);

		  // Write new application into flash
		  write_into_flash(APP_STARTING_ADDRESS,firmware,1576);

          free(FatFs);
          free(file);
		  free(buffer);
		  free(firmware);
		  f_unlink("new_Firmware.bin"); // remove the file from SD card after update
